// Send NCF alert email
async function sendNCFAlertEmail(alerts: AlertData[], recipients: string[], businessSettings: BusinessSettings): Promise<boolean> {
  try {
    const transporter = createTransporter()

    // Verify connection
    await transporter.verify()

    const emailHTML = generateNCFAlertEmailHTML(alerts, businessSettings)

    const criticalCount = alerts.filter(a => a.severity === 'DANGER' || a.severity === 'CRITICAL').length
    const subject = criticalCount > 0
      ? `üö® ALERTA CR√çTICA NCF - ${criticalCount} problema(s) requieren atenci√≥n inmediata`
      : `‚ö†Ô∏è Notificaci√≥n NCF - ${alerts.length} alerta(s) detectada(s)`

    const mailOptions = {
      from: `"${businessSettings.name} - Sistema POS" <${loadEmailConfig().user}>`,
      to: recipients.join(', '),
      subject,
      html: emailHTML
    }

    const result = await transporter.sendMail(mailOptions)
    console.log('NCF alert email sent successfully:', result.messageId)

    return true
  } catch (error) {
    console.error('Error sending NCF alert email:', error)
    return false
  }
}

/**
 * POST /api/ncf/alerts/send - Send NCF alert notifications via email
 */
export async function POST(req: NextRequest) {
  const session = await getSessionFromCookie()
  if (!session) {
    return new NextResponse(JSON.stringify({ error: "Unauthorized" }), { status: 401 })
  }

  // Only admins can send alert emails
  if (session.role !== 'ADMIN') {
    return new NextResponse(JSON.stringify({ error: "Insufficient permissions" }), { status: 403 })
  }

  try {
    const { recipients, alerts } = await req.json()

    if (!recipients || !Array.isArray(recipients) || recipients.length === 0) {
      return new NextResponse(JSON.stringify({ error: "Recipients list is required" }), { status: 400 })
    }

    if (!alerts || !Array.isArray(alerts) || alerts.length === 0) {
      return new NextResponse(JSON.stringify({ error: "Alerts list is required" }), { status: 400 })
    }

    // Get business settings
    const businessSettings = await getBusinessSettings()

    // Send email
    const emailSent = await sendNCFAlertEmail(alerts, recipients, businessSettings)

    if (emailSent) {
      // Log the email sending
      await prisma.auditLog.create({
        data: {
          userId: session.userId,
          action: 'EMAIL_SENT',
          entity: 'NCF_ALERTS',
          entityId: 'notification',
          newValue: JSON.stringify({
            recipients: recipients.length,
            alertsCount: alerts.length,
            timestamp: new Date().toISOString()
          })
        }
      })

      return NextResponse.json({
        success: true,
        message: `Notificaci√≥n enviada exitosamente a ${recipients.length} destinatario(s)`
      })
    } else {
      return new NextResponse(JSON.stringify({ error: "Failed to send email notification" }), { status: 500 })
    }

  } catch (error) {
    console.error('Error sending NCF alert notification:', error)
    return new NextResponse(JSON.stringify({ error: 'Server error' }), { status: 500 })
  }
}