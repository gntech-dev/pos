// Send automated NCF alert email
async function sendAutomatedNCFAlertEmail(alerts: AlertData[], businessSettings: BusinessSettings): Promise<boolean> {
  try {
    // Get admin users for email notifications
    const adminUsers = await prisma.user.findMany({
      where: { role: 'ADMIN' },
      select: { email: true, name: true }
    })

    const recipients = adminUsers
      .map((user: { email: string | null; name: string | null }) => user.email)
      .filter((email: string | null) => email && email.includes('@')) // Basic email validation

    if (recipients.length === 0) {
      console.warn('No valid admin email addresses found for NCF alerts')
      return false
    }

    const transporter = createTransporter()

    // Verify connection
    await transporter.verify()

    const emailHTML = generateNCFAlertEmailHTML(alerts, businessSettings)

    const criticalCount = alerts.filter(a => a.severity === 'DANGER' || a.severity === 'CRITICAL').length
    const subject = criticalCount > 0
      ? `游뚿 ALERTA CR칈TICA NCF - ${criticalCount} problema(s) requieren atenci칩n inmediata`
      : `丘멆잺 Notificaci칩n NCF Autom치tica - ${alerts.length} alerta(s) detectada(s)`

    const mailOptions = {
      from: `"${businessSettings.name} - Sistema POS" <${loadEmailConfig().user}>`,
      to: recipients.join(', '),
      subject,
      html: emailHTML
    }

    const result = await transporter.sendMail(mailOptions)
    console.log('Automated NCF alert email sent successfully:', result.messageId)

    return true
  } catch (error) {
    console.error('Error sending automated NCF alert email:', error)
    return false
  }
}

/**
 * GET /api/ncf/alerts - Get active NCF alerts
 */
export async function GET(_request: NextRequest) {
  const session = await getSessionFromCookie()
  if (!session) {
    return new NextResponse(JSON.stringify({ error: "Unauthorized" }), { status: 401 })
  }

  // Only admins and managers can access alerts
  if (!['ADMIN', 'MANAGER'].includes(session.role)) {
    return new NextResponse(JSON.stringify({ error: "Insufficient permissions" }), { status: 403 })
  }

  try {
    const sequences = await prisma.nCFSequence.findMany({
      orderBy: { type: 'asc' }
    })

    const now = new Date()
    const alerts: AlertData[] = []

    for (const sequence of sequences) {
      const expiryDate = new Date(sequence.expiryDate)
      const daysLeft = Math.ceil((expiryDate.getTime() - now.getTime()) / (1000 * 60 * 60 * 24))
      const remaining = sequence.endNumber - sequence.currentNumber

      // Check for expired sequences
      if (daysLeft < 0) {
        alerts.push({
          type: 'EXPIRED',
          message: `La secuencia NCF ${sequence.type} ha EXPIRADO. Ya no se pueden generar m치s NCF de este tipo.`,
          severity: 'DANGER',
          sequenceType: sequence.type as NCFType,
          daysLeft,
          remaining,
          actionRequired: true,
          autoGenerated: true
        })
      }
      // Check for sequences expiring in 7 days or less (Critical)
      else if (daysLeft <= 7) {
        alerts.push({
          type: 'EXPIRING_SOON',
          message: `La secuencia NCF ${sequence.type} expira en ${daysLeft} d칤a${daysLeft === 1 ? '' : 's'}. RENOVACI칍N URGENTE REQUERIDA.`,
          severity: 'CRITICAL',
          sequenceType: sequence.type as NCFType,
          daysLeft,
          remaining,
          actionRequired: true,
          autoGenerated: true
        })
      }
      // Check for sequences expiring in 30 days or less (Warning)
      else if (daysLeft <= 30) {
        alerts.push({
          type: 'EXPIRING_SOON',
          message: `La secuencia NCF ${sequence.type} expira en ${daysLeft} d칤as. Considera renovar pronto.`,
          severity: 'WARNING',
          sequenceType: sequence.type as NCFType,
          daysLeft,
          remaining,
          actionRequired: true,
          autoGenerated: true
        })
      }

      // Check for low stock sequences
      if (remaining < 20) {
        const severity = remaining < 10 ? 'CRITICAL' : 'WARNING'
        alerts.push({
          type: 'LOW_STOCK',
          message: `La secuencia NCF ${sequence.type} tiene muy pocos n칰meros disponibles (${remaining} restantes).`,
          severity,
          sequenceType: sequence.type as NCFType,
          remaining,
          actionRequired: true,
          autoGenerated: true
        })
      } else if (remaining < 100) {
        alerts.push({
          type: 'LOW_STOCK',
          message: `La secuencia NCF ${sequence.type} tiene pocos n칰meros disponibles (${remaining} restantes).`,
          severity: 'WARNING',
          sequenceType: sequence.type as NCFType,
          remaining,
          actionRequired: true,
          autoGenerated: true
        })
      }

      // Check for exhausted sequences
      if (remaining <= 0) {
        alerts.push({
          type: 'EXHAUSTED',
          message: `La secuencia NCF ${sequence.type} se ha AGOTADO. No se pueden generar m치s NCF de este tipo.`,
          severity: 'DANGER',
          sequenceType: sequence.type as NCFType,
          remaining,
          actionRequired: true,
          autoGenerated: true
        })
      }
    }

    // Sort alerts by severity (DANGER > CRITICAL > WARNING > INFO)
    const severityOrder = { 'DANGER': 4, 'CRITICAL': 3, 'WARNING': 2, 'INFO': 1 }
    alerts.sort((a, b) => severityOrder[b.severity] - severityOrder[a.severity])

    return NextResponse.json({
      data: alerts,
      summary: {
        total: alerts.length,
        danger: alerts.filter(a => a.severity === 'DANGER').length,
        critical: alerts.filter(a => a.severity === 'CRITICAL').length,
        warning: alerts.filter(a => a.severity === 'WARNING').length,
        info: alerts.filter(a => a.severity === 'INFO').length
      },
      timestamp: now.toISOString()
    })
  } catch (error) {
    console.error('Error fetching NCF alerts:', error)
    return new NextResponse(JSON.stringify({ error: 'Server error' }), { status: 500 })
  }
}

/**
 * POST /api/ncf/alerts/check - Trigger alert check (simulates cron job)
 */
export async function POST(_request: NextRequest) {
  const session = await getSessionFromCookie()
  if (!session) {
    return new NextResponse(JSON.stringify({ error: "Unauthorized" }), { status: 401 })
  }

  // Only admins can trigger automated checks
  if (session.role !== 'ADMIN') {
    return new NextResponse(JSON.stringify({ error: "Insufficient permissions" }), { status: 403 })
  }

  try {
    const sequences = await prisma.nCFSequence.findMany()
    const now = new Date()
    const alerts: AlertData[] = []
    let criticalIssues = 0

    for (const sequence of sequences) {
      const expiryDate = new Date(sequence.expiryDate)
      const daysLeft = Math.ceil((expiryDate.getTime() - now.getTime()) / (1000 * 60 * 60 * 24))
      const remaining = sequence.endNumber - sequence.currentNumber

      if (daysLeft < 0 || remaining <= 0) {
        criticalIssues++
      }

      // Generate alerts for critical issues
      if (daysLeft < 0) {
        alerts.push({
          type: 'EXPIRED',
          message: `CR칈TICO: Secuencia NCF ${sequence.type} EXPIRADA. Sistema pausado para este tipo.`,
          severity: 'DANGER',
          sequenceType: sequence.type as NCFType,
          daysLeft,
          remaining,
          actionRequired: true,
          autoGenerated: true
        })
      } else if (remaining <= 0) {
        alerts.push({
          type: 'EXHAUSTED',
          message: `CR칈TICO: Secuencia NCF ${sequence.type} AGOTADA. No se pueden generar m치s NCF.`,
          severity: 'DANGER',
          sequenceType: sequence.type as NCFType,
          remaining,
          actionRequired: true,
          autoGenerated: true
        })
      } else if (daysLeft <= 7) {
        alerts.push({
          type: 'EXPIRING_SOON',
          message: `URGENTE: NCF ${sequence.type} expira en ${daysLeft} d칤as. Renovar inmediatamente.`,
          severity: 'CRITICAL',
          sequenceType: sequence.type as NCFType,
          daysLeft,
          remaining,
          actionRequired: true,
          autoGenerated: true
        })
      }
    }

    // Log the automated check
    await prisma.auditLog.create({
      data: {
        userId: session.userId,
        action: 'AUTO_CHECK',
        entity: 'NCF',
        entityId: 'alerts',
        newValue: JSON.stringify({
          alertsGenerated: alerts.length,
          criticalIssues,
          sequencesChecked: sequences.length,
          timestamp: now.toISOString()
        })
      }
    })

    // TODO: Here you could integrate with email notifications, SMS, or push notifications
    // For now, we just return the alerts

    // Send automated email notifications for critical alerts
    const criticalAlerts = alerts.filter(a => a.severity === 'DANGER' || a.severity === 'CRITICAL')
    if (criticalAlerts.length > 0) {
      try {
        const businessSettings = await getBusinessSettings()
        const emailSent = await sendAutomatedNCFAlertEmail(criticalAlerts, businessSettings)
        if (emailSent) {
          console.log(`Automated NCF alert email sent for ${criticalAlerts.length} critical issues`)
        }
      } catch (emailError) {
        console.error('Failed to send automated NCF alert email:', emailError)
        // Don't fail the entire request if email fails
      }
    }

    return NextResponse.json({
      success: true,
      alerts,
      criticalIssues,
      sequencesChecked: sequences.length,
      emailSent: criticalAlerts.length > 0,
      message: criticalIssues > 0 
        ? `Se detectaron ${criticalIssues} problema(s) cr칤tico(s) que requieren atenci칩n inmediata. ${criticalAlerts.length > 0 ? 'Notificaci칩n por email enviada.' : ''}`
        : 'Verificaci칩n autom치tica completada sin problemas cr칤ticos.'
    })
  } catch (error) {
    console.error('Error during automated NCF alert check:', error)
    return new NextResponse(JSON.stringify({ error: 'Server error' }), { status: 500 })
  }
}